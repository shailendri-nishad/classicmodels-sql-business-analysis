/* Q18. Identify how much each customer contributes to total company revenue. */

-- Output Columns: customerNumber, customerName, customer_revenue, revenue_percentage

SELECT c.customerNumber, 
	   c.customerName,
       SUM(od.quantityOrdered * od.priceEach) AS customer_revenue,
       ROUND(
			SUM(od.quantityOrdered * od.priceEach) * 100 
            / SUM(SUM(od.quantityOrdered * od.priceEach)) OVER(),
            2) AS revenue_percentage
FROM customers c 
JOIN orders o 
	ON c.customerNumber = o.customerNumber
JOIN orderdetails od
	ON o.orderNumber = od.orderNumber
WHERE o.status = 'Shipped'
GROUP BY c.customerNumber, c.customerName;


/* Q19. HR wants to visualize the reporting hierarchy of the sales organization. 
Display each employee alongside the name of their direct manager. */

-- Output Columns: employeeNumber, employeeName, jobTitle, managerName

SELECT e.employeeNumber,
	   CONCAT(e.firstName, ' ', e.lastName) AS employeeName,
       e.jobTitle,
       COALESCE(CONCAT(m.firstName, ' ', m.lastName), 'No Manager') AS managerName
FROM employees e 
LEFT JOIN employees m			-- Self-join: employees table joined to itself
	ON e.reportsTo = m.employeeNumber;


/* Q20. The customer success team wants to flag accounts that have had at least one 
cancelled order, so they can be prioritized for follow-up. */

-- Output Columns: customerNumber, customerName, country

SELECT c.customerNumber, 
	   c.customerName,
       c.country
FROM customers c 
WHERE EXISTS(
			SELECT 1
            FROM orders o 
            WHERE o.customerNumber = c.customerNumber
            AND o.status = 'Cancelled'
);


/* Q21. Marketing wants a single unified contact list combining both customers 
and employees, tagged by their role, for an upcoming company-wide mailer. */

-- Output Columns: full_name, email_or_phone, contact_type

SELECT 
    c.customerName AS full_name,
    c.phone AS email_or_phone,
    'Customer' AS contact_type
FROM customers c

UNION            -- Combines two result sets with the same column structure

SELECT 
    CONCAT(e.firstName, ' ', e.lastName) AS full_name,
    e.email AS email_or_phone,
    'Employee' AS contact_type
FROM employees e
ORDER BY full_name;


/* Q22. The reporting team wants a reusable, always-up-to-date summary of 
customer order performance without rerunning complex joins every time. */

-- Creates a VIEW: customer_order_summary

CREATE VIEW customer_order_summary AS
SELECT 
    c.customerNumber,
    c.customerName,
    c.country,
    COUNT(DISTINCT o.orderNumber) AS total_orders,
    SUM(od.quantityOrdered * od.priceEach) AS total_revenue
FROM customers c
LEFT JOIN orders o 
    ON c.customerNumber = o.customerNumber
	AND o.status = 'Shipped'
LEFT JOIN orderdetails od 
    ON o.orderNumber = od.orderNumber
GROUP BY c.customerNumber, c.customerName, c.country;

-- Usage example:
SELECT * FROM customer_order_summary
ORDER BY total_revenue DESC;


/* Q23. Inventory managers want to identify "dead stock" — products that have 
never been ordered by any customer, so they can be reviewed for discontinuation. */

-- Output Columns: productCode, productName, productLine, quantityInStock

SELECT 
    p.productCode,
    p.productName,
    p.productLine,
    p.quantityInStock
FROM products p
LEFT JOIN orderdetails od 
    ON p.productCode = od.productCode
WHERE od.productCode IS NULL      -- Anti-join pattern: no matching order line exists
ORDER BY p.quantityInStock DESC;