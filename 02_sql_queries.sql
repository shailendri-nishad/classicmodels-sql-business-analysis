/* Q11. Rank sales representatives based on the revenue generated from their assigned customers. */

-- Output Columns: employeeNumber, employeeName, total_revenue, sales_rank 

SELECT e.employeeNumber,
	   CONCAT(e.firstName, ' ', e.lastName) AS employeeName,
       SUM(od.quantityOrdered * od.priceEach) AS total_revenue,
       DENSE_RANK() OVER(ORDER BY SUM(od.quantityOrdered * od.priceEach) DESC) AS sales_rank
FROM employees AS e
JOIN customers AS c
	ON e.employeeNumber = c.salesRepEmployeeNumber		-- Connects employee to their assigned customers
JOIN orders AS o
	ON o.customerNumber = c.customerNumber		-- Connects customers to their placed orders
JOIN orderDetails AS od
	ON o.orderNumber = od.orderNumber		-- Connects orders to their pricing details
WHERE o.status = 'Shipped'		-- Filters for completed sales only
GROUP BY e.employeeNumber, e.firstName, e.lastName
ORDER BY sales_rank ASC;		-- Displays the top-performing employee (Rank 1) first


/* Q12. Regional managers want to compare office performance. Calculate total revenue generated by each office location. */

-- Output Columns: officeCode, city, country, total_customers, total_revenue

