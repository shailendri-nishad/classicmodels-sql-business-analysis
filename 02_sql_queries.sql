/* Q1. The management team wants to understand the overall financial performance of the company. 
 Calculate the total revenue generated from all completed orders using quantity ordered and selling price. */

-- Output Columns : total_revenue

SELECT SUM(od.quantityOrdered * od.priceEach) AS total_revenue
FROM orderdetails od
JOIN orders o 
	ON od.orderNumber = o.orderNumber
WHERE o.status = 'Shipped';			-- Filter only completed (shipped) orders


/* Q2. Management wants to understand market presence across countries. Count customers in each country. */

-- Output Columns: country, total_customers

SELECT country,
	   COUNT(*) AS total_customers
FROM customers 
GROUP BY country
ORDER BY total_customers DESC;


/* Q3. The finance department wants to monitor how revenue changes over time. 
Calculate monthly revenue and display the trend year-wise and month-wise. */

-- Output Columns : order_year, order_month, monthly revenue

SELECT YEAR(o.orderDate) AS order_year,
	   MONTH(o.orderDate) AS order_month,
	   SUM(od.quantityOrdered * od.priceEach) AS monthly_revenue
FROM orders o 
JOIN orderdetails od
	ON o.orderNumber = od.orderNumber
WHERE o.status = 'Shipped'			-- Filter only completed (shipped) orders
GROUP BY order_year, 
		 order_month
ORDER BY order_year ASC, 
		 order_month ASC;


/* Q4. The company operates globally and wants to identify which countries contribute the most revenue.
Calculate total revenue generated by customers from each country. */

-- Output Columns: country, total_revenue

SELECT c.country,
	   SUM(od.quantityOrdered * od.priceEach) AS total_revenue
FROM customers AS c
JOIN orders AS o
	ON c.customerNumber = o.customerNumber
JOIN orderDetails AS od
	ON o.orderNumber = od.orderNumber
WHERE o.status = 'Shipped'			-- Filter only completed (shipped) orders
GROUP BY country
ORDER BY total_revenue DESC;


/* Q5. The marketing team wants to categorize customers based on their spending behavior to identify high-value 
customers for loyalty programs and targeted campaigns. */

-- Output Columns: customerNumber, customerName, country, total_orders, total_revenue, customer_segment

SELECT c.customerNumber, 
	   c.customerName, 
       c.country, 
       COUNT(DISTINCT o.orderNumber) AS total_orders, 
       SUM(od.quantityOrdered * od.priceEach) AS total_revenue, 
       CASE
			WHEN SUM(od.quantityOrdered * od.priceEach) > 100000 THEN 'Platinum'
            WHEN SUM(od.quantityOrdered * od.priceEach) BETWEEN 50000 AND 100000 THEN 'Gold'
            ELSE 'Silver'
	   END AS customer_segment
FROM customers AS c
JOIN orders AS o
	ON c.customerNumber = o.customerNumber
JOIN orderdetails AS od
	ON o.orderNumber = od.orderNumber
WHERE o.status = 'Shipped'			-- Filter only completed (shipped) orders
GROUP BY c.customerNumber, 
	     c.customerName, 
         c.country
ORDER BY total_revenue DESC;


/* Q6. The sales team wants to re-engage inactive customers. 
Find customers who are registered in the system but have never placed an order. */

-- Output Columns: customerNumber, customerName, country 

SELECT c.customerNumber, 
	   c.customerName, 
       c.country
FROM customers c
WHERE c.customerNumber NOT IN (
		SELECT DISTINCT customerNumber 
        FROM orders 
        WHERE customerNumber IS NOT NULL
);


