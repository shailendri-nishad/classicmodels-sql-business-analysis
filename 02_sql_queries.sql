	ON o.orderNumber = od.orderNumber
WHERE o.status = 'Shipped'
GROUP BY 
    ofc.officeCode,
    ofc.city,
    ofc.country
ORDER BY total_revenue DESC;


/* Q13. The product team wants to identify star products in every category. 
Find the highest revenue-generating product within each product line. */

-- Output Columns: productLine, productName, total_revenue, rank

WITH ProductRevenue AS (
		SELECT p.productLine,
			   p.productName,
			   SUM(od.quantityOrdered * od.priceEach) AS total_revenue,
			   DENSE_RANK() OVER (PARTITION BY p.productLine 
								  ORDER BY SUM(od.quantityOrdered * od.priceEach) DESC) AS `rank`
    FROM products p
    JOIN orderdetails od 
		ON p.productCode = od.productCode
	JOIN orders o 
		ON o.orderNumber = od.orderNumber
	WHERE o.status = 'Shipped'
    GROUP BY p.productLine, 
			 p.productName)
SELECT 
    productLine,productName, total_revenue, `rank`
FROM 
    ProductRevenue
WHERE 
    `rank` = 1;


/* Q14. Regional sales managers want to identify their best customers. 
Rank customers by revenue within each country and find the top 3 customers for each country. */

-- Output Columns: country, customerName, total_revenue, country_rank

WITH CustomerRevenue AS (
	SELECT c.country,
		   c.customerName,
           SUM(od.quantityOrdered * od.priceEach) AS total_revenue,
           ROW_NUMBER() OVER(PARTITION BY c.country ORDER BY SUM(od.quantityOrdered * od.priceEach) DESC) AS country_rank
FROM customers c
JOIN orders o 
	ON c.customerNumber = o.customerNumber
JOIN orderdetails od
	ON o.orderNumber = od.orderNumber
WHERE o.status = 'Shipped'
GROUP BY c.country,
		 c.customerNumber, -- Ensures acuracy if two customers shares a name
         c.customerName)

SELECT country,
	   customerName,
	   total_revenue,
	   country_rank
FROM CustomerRevenue
WHERE country_rank <=3
ORDER BY country ASC,
		 country_rank ASC;


/* Q15. Finance team wants month-over-month growth AND cumulative revenue 
so far this year, in one view. */

-- Output Columns: month, monthly_revenue, previous_month_revenue, growth_percentage, running_total_revenue

WITH MonthlyRevenue AS 
				(SELECT DATE_FORMAT(paymentDate, '%Y-%m') AS `month`,
					    SUM(amount) AS monthly_revenue
                FROM payments
                GROUP BY DATE_FORMAT(paymentDate, '%Y-%m')),

CalculatedGrowth AS 
				(SELECT `month`,
						monthly_revenue,
					    LAG(monthly_revenue, 1) OVER (ORDER BY `month`) AS previous_month_revenue,
                        SUM(monthly_revenue) OVER (ORDER BY `month` ROWS UNBOUNDED PRECEDING) AS running_total_revenue
                FROM MonthlyRevenue)

SELECT `month`,
	   monthly_revenue,
       previous_month_revenue,
       ROUND(((monthly_revenue - previous_month_revenue)/previous_month_revenue * 100),2) AS growth_percentage,
       running_total_revenue
FROM CalculatedGrowth
ORDER BY `month` ASC;


/* Q16. The company wants to identify customers who spend more per order. 
Calculate the average order value for each customer and rank them from highest to lowest. */

-- Output Columns: customerNumber, customerName, avg_order_value 

SELECT 
    c.customerNumber,
    c.customerName,
    ROUND(AVG(OrderTotals.order_total), 2) AS avg_order_value
FROM customers c
JOIN orders o 
	ON c.customerNumber = o.customerNumber
JOIN (
    -- This subquery calculates the total value for each individual order
    SELECT 
        orderNumber, 
        SUM(quantityOrdered * priceEach) AS order_total
    FROM 
        orderdetails
    GROUP BY 
        orderNumber
) AS OrderTotals 
	ON o.orderNumber = OrderTotals.orderNumber
WHERE o.status = 'Shipped'
GROUP BY 
    c.customerNumber,
    c.customerName
ORDER BY 
    avg_order_value DESC;


