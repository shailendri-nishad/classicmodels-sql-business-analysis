/* Q8. Inventory planning depends on product demand.
Identify the top-selling products based on the total quantity sold and rank the products in descending order of quantity sold. */

-- Output Columns: productCode, productName, quantity_sold, product_rank

SELECT p.productCode,
	   p.productName,
       SUM(od.quantityOrdered) AS quantity_sold,
       DENSE_RANK() OVER(ORDER BY SUM(od.quantityOrdered) DESC) AS product_rank
FROM products p
JOIN orderDetails od
	ON p.productCode = od.productCode
JOIN orders o
	ON o.orderNumber = od.orderNumber 
WHERE o.status = 'Shipped'			-- Filter only completed (shipped) orders
GROUP BY p.productCode, p.productName
ORDER BY product_rank ASC;


